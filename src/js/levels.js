  // Key:
  // 'S-[suffix]' = start cell 
  //      'D' = start down
  //      'L' = start left
  //      'R' = start right
  //      'U' = start up
  // '.' = empty cell
  // '#' = wall
  // 'T' = target
  // 'B' = bomb
  // 'P-[suffix]'
  //      'A' = portal A
  //      'B' = portal B
  // 'F-[suffix]'
  //      'R' = red filter
  //      'G' = green filter
  //      'B' = blue filter
  // 'M-[suffix]' = 
  //      '/' = / mirror
  //      '\\' = \ mirror

export function getLevelDims(level) {
  const rows = level.layout.length;
  const cols = level.layout[0].length;
  return { rows, cols };
}

